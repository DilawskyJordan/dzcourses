<p><font><b>1.2 - باستخدامك للموقع ، فإنك تشير إلى قبولك للشروط وأنك توافق على الالتزام بها. عدم الرضا عن الموقع أو محتوياته هو التوقف عن استخدام الموقع.</b></font></p>
