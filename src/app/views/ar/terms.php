<h3 id="courses" class="western"><font><b>4 – الدورات</b></font></h3>
<p>


</p>
<p><font><b>4.1 - الحد الأقصى لحجم الدورة 2.5 غيغابايت.</b></font></p>
<p><font><b>4.2 - يجب أن تحتوي الدورة على 3 مقاطع فيديو على الأقل.
</b></font></p>
<p><font><b>4.3 - يجب أن يتبع عنوان جميع مقاطع الفيديو هذا النمط: 01 - عنوان الفيديو الأول / 02 - عنوان الفيديو الثاني / N - XXXX.</b></font></p>
<p><font><b>4.4 - مشاهدة الفيديوين الأول والثاني مجانًا.</b></font></p>
<p><font><b>4.5 - يجب أن يكون الفيديو الأول مقدمة لدورتك.</b></font></p>
<p><font><b>4.6 -  يجب أن يكون الفيديو الثاني فيديو تعليمي.</b></font></p>
<p><font><b>4.7 - يجب أن تذكر اسمك في فيديو المقدمة.</b></font></p>
<p><font><b>4.8 - يجب رفع الفيديوهات كملف ZIP.</b></font></p>
<p><font><b>4.9 - جب أن يكون صورة الحائط الدورة بتنسيق png و jpg و jpeg و gif file.</b></font></p>
<p><font><b>4.10 - يجب عدم استخدام صور يدون حقوق النشر.</b></font></p>
<p><font><b>4.11 - إذا كان ملف الدورة التدريبية يحتوي على برامج ضارة ، فسيتم إبلاغك.</b></font></p>
<p><font><b>4.12 - يجب أن تكون لغة الدورة باللغة العربية أو الفرنسية أو الإنجليزية.</b></font></p>
<p><font><b>4.13 - يجب عليك استخدام وصف شامل للغاية لدورتك.</b></font></p>
<p><font><b>4.14 - غير مسموح بدورات مسروقة.</b></font></p>
<p><font><b>4.15 - لا يمكنك إزالة دورتك بمجرد رفعها على الموقع.</b></font></p>
<p><font><b>4.16 - إذا حصلت الدورة على تقييم أقل أو يساوي 3/10 ، فسيتم حذفها.</b></font></p>
<p><font><b>4.17 - يتم قبول الدورات على حسب جودة محتواها.</b></font></p>
